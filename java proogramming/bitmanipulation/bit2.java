public class bit2 {
    public static void main(String args[]){
        System.out.println((5&6));
        System.out.println((5|6));      //pipe operator   
        System.out.println((5^6));
        System.out.println((~6));
        System.out.println((5<<2));
        System.out.println((6<<4));
    }
    
}
