import java.util.*;
public class switch1{
    public static void main(String args[]){
        //basic switch code
        int number = 1;
        switch(number){
            case 1 : System.out.println("grapes");
            break;
            case 2 : System.out.println("mango");
            
            case 3 : System.out.println("lichi");
            case 4 : System.out.println("blue berry");
           
            case 5 : System.out.println("avocado");
            default :  System.out.println("not found");
        }

    }
}
